#include <iostream>
using namespace std;
int main()
{
	float i,a;
	cout<<"请输入当月利润:";
	cin>>i;
	if (i<=100000)
		a=i*0.1;
	else {
		if(i>100000||i<=200000)
			a=10000+(i-100000)*0.075;
		else{
			if(i>200000||i<=400000)
				a=10000+100000*0.075+(i-200000)*0.05;
			else{
				if(i>400000||i<=600000)
					a=10000+100000*0.075+200000*0.05+(i-400000)*0.03;
				else{
					if(i>600000||i<=1000000)
						a=10000+100000*0.075+200000*0.05+400000*0.03+(i-600000)*0.015;
					else a=10000+100000*0.075+200000*0.05+400000*0.03+600000*0.015+(i-1000000)*0.01;
				}
			}
		}
	}
		   cout<<"应发奖金总额为："<<a<<endl;
	return 0;
}
